import importlib

try:
    BaseSettings = importlib.import_module("pydantic_settings").BaseSettings
except ModuleNotFoundError:  # Fallback for environments without pydantic-settings
    from pydantic.v1 import BaseSettings


class Settings(BaseSettings):
    """
    Application settings management with environment variables support
    """
    
    # Application
    APP_NAME: str = "ALIE - AI Lead Intelligence Engine"
    APP_VERSION: str = "1.0.0"
    ENVIRONMENT: str = "development"
    DEBUG: bool = True
    LOG_LEVEL: str = "INFO"
    
    # API
    API_V1_STR: str = "/api/v1"
    API_TITLE: str = "ALIE API"
    API_DESCRIPTION: str = "AI Lead Intelligence Engine for Local Business Recommendations"
    
    # Database
    NEO4J_URI: str = "bolt://localhost:7687"
    NEO4J_USER: str = "neo4j"
    NEO4J_PASSWORD: str = "password123"
    
    DATABASE_URL: str = "postgresql://alie_user:postgres123@localhost:5432/alie_db"
    DB_POOL_SIZE: int = 20
    DB_MAX_OVERFLOW: int = 0
    
    # Redis
    REDIS_URL: str = "redis://localhost:6379"
    REDIS_DB: int = 0
    REDIS_CACHE_TTL: int = 3600
    
    # Celery
    CELERY_BROKER_URL: str = "redis://localhost:6379/1"
    CELERY_RESULT_BACKEND: str = "redis://localhost:6379/2"
    
