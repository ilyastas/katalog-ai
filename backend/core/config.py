    DATA_INDEX_URL: str = "https://ilyastas.github.io/katalog-ai/data/index.json"
    
    # Verification
    VERIFICATION_TIMEOUT: int = 30
    MAX_RETRIES: int = 3
    
    # Analytics
    TRACK_EVENTS: bool = True
    ANALYTICS_DB: str = "alie_db"
    ANALYTICS_TABLE: str = "lead_events"
    
